function makeAnalysis(question, knowledge) {
  const ans = answerText(question, question.answer);
  const base = `本题答案：${ans}。`;
  if (question.analysis) return `${base}${question.analysis}`;
  const direct = directAnalysis(question);
  if (direct) return `${base}${direct}`;
  if (question.type === "判断题") {
    if (question.answer.includes("A")) return `${base}这句话整体成立。${knowledge.memory}`;
    return `${base}题干里有不准确的地方，所以整句不能判对。${knowledge.memory}`;
  }
  return `${base}${knowledge.memory}`;
}

function getGroups(list) {
  if (mode === "random") return new Map([["随机检验卷", list]]);
  return [...list].sort((a, b) => sectionOrder(a.section) - sectionOrder(b.section) || a.id - b.id)
    .reduce((groups, question) => {
      if (!groups.has(question.section)) groups.set(question.section, []);
      groups.get(question.section).push(question);
      return groups;
    }, new Map());
}

function answerText(question, keys) {
  if (!keys || keys.length === 0) return "待校对";
  return keys.map((key) => {
    const option = question.options.find((item) => item.key === key);
    return option ? `${option.key}. ${option.label}` : key;
  }).join("；");
}

function isCorrect(question, selected) {
  if (!selected || selected.length !== question.answer.length) return false;
  return [...selected].sort().join("|") === [...question.answer].sort().join("|");
}

function renderSections() {
  const groups = getGroups(activeQuestions);
  questionCount.textContent = `${activeQuestions.length} 题`;
  paperFlow.innerHTML = [...groups.entries()].map(([section, items]) => `
    <section class="paper-section" id="${section}">
      <div class="section-title">
        <div><p class="eyebrow">${mode === "random" ? "随机练习" : "题型模块"}</p><h3>${section}</h3></div>
        <span>${items.length} 题</span>
      </div>
      <div class="question-stack">${items.map((question, index) => renderQuestionCard(question, index)).join("")}</div>
    </section>
  `).join("");
  sectionNav.innerHTML = [...groups.entries()].map(([section, items]) => `
    <a class="section-link" href="#${section}"><span>${section}</span><b>${items.length} 题</b></a>
  `).join("");
  bindQuestionEvents();
  renderMath();
}

function renderQuestionCard(question, indexInSection) {
  const selected = selectedAnswers.get(question.id) || [];
  const submitted = submittedAnswers.has(question.id);
  const correct = isCorrect(question, selected);
  const title = mode === "random" ? `第 ${indexInSection + 1} 题` : `${question.section} ${indexInSection + 1}`;
  const knowledge = detectKnowledge(question);
  const formulaBlock = knowledge.formulas.length ? `<div class="formula-box">
    <div class="formula-heading"><span>关键公式</span><b>${knowledge.formulaName}</b></div>
    <p>${knowledge.formulaNote}</p>
    ${knowledge.formulas.map((formula) => `<div class="math-line">${formula}</div>`).join("")}
    ${knowledge.symbols.length ? `<div class="symbol-list"><span>符号说明</span><dl>${knowledge.symbols.map(([s, m]) => `<div><dt>${s}</dt><dd>${m}</dd></div>`).join("")}</dl></div>` : ""}
  </div>` : "";
  return `
    <article class="question-card ${question.needsReview ? "reviewing" : ""}" id="q-${question.id}">
      <div class="question-head">
        <div><span class="pill">${question.type}</span><h4>${title}</h4></div>
        <span class="source-id">${question.needsReview ? "需校对" : `原题 ${question.id}`}</span>
      </div>
      <p class="question-text">${question.text}</p>
      <div class="options">
        ${question.options.map((option) => {
          const isSelected = selected.includes(option.key);
          const isAnswer = question.answer.includes(option.key);
          const stateClass = submitted ? (isAnswer ? "correct" : (isSelected ? "wrong" : "")) : "";
          return `<button class="option ${isSelected ? "selected" : ""} ${stateClass}" type="button" data-id="${question.id}" data-answer="${option.key}"><span class="key">${option.key}</span><span>${option.label}</span></button>`;
        }).join("")}
      </div>
      <div class="answer-actions">
        <button class="ghost-action" type="button" data-review="${question.id}">${reviewSet.has(question.id) ? "取消待复习" : "标记待复习"}</button>
      </div>
      <div class="answer-panel" ${submitted ? "" : "hidden"}>
        <div><span>正确答案</span><strong>${answerText(question, question.answer)}</strong></div>
        <div><span>你的选择</span><strong>${answerText(question, selected)}</strong></div>
        <b class="${selected.length ? (correct ? "ok" : "bad") : ""}">${selected.length ? (correct ? "回答正确" : "再看一遍") : "先试着选"}</b>
      </div>
      <div class="explain-panel" ${submitted ? "" : "hidden"}>
        <div class="explain-title"><p class="eyebrow">知识点讲解</p><h5>${knowledge.formulaName}</h5></div>
        ${formulaBlock}
        <p>${knowledge.explanation}</p>
        <div class="analysis-box"><span>本题为什么这样选</span><p>${makeAnalysis(question, knowledge)}</p></div>
      </div>
    </article>
  `;
}

function bindQuestionEvents() {
  paperFlow.querySelectorAll(".option").forEach((button) => {
    button.addEventListener("click", () => {
      const id = Number(button.dataset.id);
      const question = activeQuestions.find((item) => item.id === id);
      const current = selectedAnswers.get(id) || [];
      if (question.type === "多选题") {
        selectedAnswers.set(id, current.includes(button.dataset.answer) ? current.filter((key) => key !== button.dataset.answer) : [...current, button.dataset.answer]);
      } else {
        selectedAnswers.set(id, [button.dataset.answer]);
      }
      submittedAnswers.add(id);
      renderSections();
      document.querySelector(`#q-${id}`)?.scrollIntoView({ block: "center" });
    });
  });
  paperFlow.querySelectorAll("[data-review]").forEach((button) => {
    button.addEventListener("click", () => {
      const id = Number(button.dataset.review);
      if (reviewSet.has(id)) reviewSet.delete(id); else reviewSet.add(id);
      renderSections();
    });
  });
}

function shuffle(list) {
  const copy = [...list];
  for (let i = copy.length - 1; i > 0; i -= 1) {
    const j = Math.floor(Math.random() * (i + 1));
    [copy[i], copy[j]] = [copy[j], copy[i]];
  }
  return copy;
}


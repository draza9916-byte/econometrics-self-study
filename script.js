function sectionOrder(section) {
  return ["判断题", "多选题", "单选题"].indexOf(section);
}

function detectKnowledge(question) {
  if (question.knowledgeKey && KNOWLEDGE[question.knowledgeKey]) return KNOWLEDGE[question.knowledgeKey];
  const text = `${question.text} ${question.knowledge || ""}`;
  if (/等级相关|Spearman/.test(text)) return KNOWLEDGE.rank;
  if (/残差平方和|拟合优度|可决系数|R²|R2|R\^2|SSR|SSE|SST/.test(text)) return /调整|n=|解释变量/.test(text) ? KNOWLEDGE.r2 : KNOWLEDGE.sse;
  if (/序列相关|自相关|DW|Durbin/.test(text)) return /DW/.test(text) ? KNOWLEDGE.dw : KNOWLEDGE.serial;
  if (/加权最小二乘|WLS|权函数|权数/.test(text)) return KNOWLEDGE.wls;
  if (/异方差/.test(text)) return /无偏|最小方差|有效|OLS|普通最小二乘/.test(text) ? KNOWLEDGE.ols : KNOWLEDGE.hetero;
  return KNOWLEDGE.default;
}

